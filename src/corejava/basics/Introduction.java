package corejava.basics;

/**
 * Introduction.java
 *
 * This class is created to understand:
 * 1. What is Programming?
 * 2. What is Java?
 * 3. Who invented Java and when?
 * 4. Full form of Java
 * 5. Advantages and Disadvantages of Java
 * 6. Different technologies in Java (Java SE, EE, ME)
 * 7. What is Core Java and why it is popular?
 */

public class Introduction {

    public static void main(String[] args) {

        System.out.println("=== Introduction to Programming ===\n");

        System.out.println("1. What is Programming?");
        System.out.println("Programming is giving clear step-by-step instructions to the computer");
        System.out.println("to solve a problem. It is like writing a recipe for the computer.\n");

        System.out.println("2. What is Java?");
        System.out.println("Java is a popular Object-Oriented Programming Language.");
        System.out.println("It is used to develop all kinds of applications like desktop, web, mobile, etc.\n");

        System.out.println("3. Who invented Java and when?");
        System.out.println("Java was created by James Gosling at Sun Microsystems.");
        System.out.println("Work started in 1991 (originally named 'Oak').");
        System.out.println("Renamed to Java in 1995 and Java 1.0 released in 1996.");
        System.out.println("Now maintained by Oracle.\n");

        System.out.println("4. Full form of Java?");
        System.out.println("Java has no official full form.");
        System.out.println("But its slogan is: \"Write Once, Run Anywhere\" (WORA).\n");

        System.out.println("5. Advantages of Java:");
        System.out.println("- Simple and easy to learn");
        System.out.println("- Object-Oriented (treats everything as real-world objects)");
        System.out.println("- Platform Independent (Write once, run on any OS)");
        System.out.println("- Secure and Robust");
        System.out.println("- Good for large applications like banking and e-commerce\n");

        System.out.println("Disadvantages of Java:");
        System.out.println("- GUI (User Interface) was not very attractive in older versions");
        System.out.println("- Slightly slower than languages like C++ because it runs on JVM\n");

        System.out.println("6. Different Technologies in Java:");
        System.out.println("- Java SE (Standard Edition)   → Standalone & Desktop applications");
        System.out.println("- Java EE (Enterprise Edition) → Web, Banking, E-commerce applications");
        System.out.println("- Java ME (Micro Edition)      → Small devices like ATM, remote controls\n");

        System.out.println("7. What is Core Java?");
        System.out.println("Core Java means the basic fundamentals of Java programming.");
        System.out.println("It includes: Variables, Data Types, Operators, Conditions, Loops,");
        System.out.println("Methods, OOP (Class, Object, Inheritance, etc.), Arrays, Exception Handling.");
        System.out.println("Core Java is the foundation for all Java technologies.");
        System.out.println("It is very popular because it is simple, secure, and used everywhere.\n");

        System.out.println("=== Summary ===");
        System.out.println("Programming = Giving instructions to computer");
        System.out.println("Java = Object-Oriented, Platform Independent language");
        System.out.println("Core Java = Basic building blocks of Java (what we are learning now)");

        System.out.println("\nTip: Keep revising these points. They are very important for interviews.");
    }
}